public class Car {
    int horsepower;
    double engineVolume;
    String brand;
    String model;
    int year;
    int maxSpeed;
    double price;

    public Car(int horsepower, double engineVolume, String brand, String model, int year, int maxSpeed, double price) {
        this.horsepower = horsepower;
        this.engineVolume = engineVolume;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.maxSpeed = maxSpeed;
        this.price = price;
    }
}